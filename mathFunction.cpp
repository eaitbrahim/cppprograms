#include <iostream>
#include <cmath>

int main(){
  std::cout << std::max(3, 5) << "\n";
  std::cout << std::min(3, 4) << "\n";
  std::cout << std::pow(2, 4) << "\n";
  std::cout << std::sqrt(9) << "\n";
  std::cout << std:: abs(-3) << "\n";
  std::cout << std::round(3.14) << "\n";
  std::cout << std::ceil(3.99) << "\n";
  return 0;
}